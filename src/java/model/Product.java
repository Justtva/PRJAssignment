package model;

import java.sql.Date;

/**
 *
 * @author Bi
 */
public class Product {
    private int id;
    private String name;
    private float import_price;
    private float sell_price;
    private int quantity;
    private Date importDate;
    private Date expireDate;
    private Shipper shipper;

    public Shipper getShipper() {
        return shipper;
    }

    public void setShipper(Shipper shipper) {
        this.shipper = shipper;
    }

   



    public Product(int id, String name, float import_price, float sell_price, int quantity, Date importDate, Date expireDate) {
        this.id = id;
        this.name = name;
        this.import_price = import_price;
        this.sell_price = sell_price;
        this.quantity = quantity;
        this.importDate = importDate;
        this.expireDate = expireDate;
    }

    public Product() {
    }

    
    public Date getImportDate() {
        return importDate;
    }

    public void setImportDate(Date importDate) {
        this.importDate = importDate;
    }

    public Date getExpireDate() {
        return expireDate;
    }

    public void setExpireDate(Date expireDate) {
        this.expireDate = expireDate;
    }

    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getImport_price() {
        return import_price;
    }

    public void setImport_price(float import_price) {
        this.import_price = import_price;
    }

    public float getSell_price() {
        return sell_price;
    }

    public void setSell_price(float sell_price) {
        this.sell_price = sell_price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
}
